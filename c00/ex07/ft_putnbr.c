#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	digit;
	long	n;

	n = nb;
	if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
	}
	digit = n % 10 + '0';
	write(1, &digit, 1);
}
